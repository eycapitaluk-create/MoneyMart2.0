const DIVIDEND_STOCK_BY_SYMBOL = new Map(
  (Array.isArray(DIVIDEND_STOCK_UNIVERSE) ? DIVIDEND_STOCK_UNIVERSE : [])
    .map((row) => [String(row?.symbol || '').trim().toUpperCase(), row])
    .filter(([symbol]) => Boolean(symbol)),
)

const DIVIDEND_STOCK_SEARCH_POOL = (Array.isArray(DIVIDEND_STOCK_UNIVERSE) ? DIVIDEND_STOCK_UNIVERSE : [])
  .map((row) => ({
    row,
    symbol: String(row?.symbol || '').trim().toUpperCase(),
    name: String(row?.name || '').trim().toLowerCase(),
  }))
  .filter((x) => x.symbol)

const normalizeMonthList = (months = []) => (
  [...new Set(
    (Array.isArray(months) ? months : [])
      .map((m) => Number(m))
      .filter((m) => Number.isInteger(m) && m >= 1 && m <= 12),
  )].sort((a, b) => a - b)
)

const toPresetDividends = (row) => {
  const months = normalizeMonthList(row?.dividendMonths)
  const amount = Math.max(0, Number(row?.dividendAmountPerShare || 0))
  if (months.length === 0) return []
  return months.map((m) => ({ month: m, amount }))
}

export function lookupDividendStockBySymbol(input = '') {
  const symbol = String(input || '').trim().toUpperCase()
  if (!symbol) return null
  const direct = DIVIDEND_STOCK_BY_SYMBOL.get(symbol)
  const jp = /^\d{4}$/.test(symbol) ? DIVIDEND_STOCK_BY_SYMBOL.get(`${symbol}.T`) : null
  const row = direct || jp
  if (!row) return null
  return {
    ...row,
    dividends: toPresetDividends(row),
  }
}

export function searchDividendStockUniverse(query = '', limit = 12) {
  const q = String(query || '').trim().toLowerCase()
  if (!q) return []
  const parts = q.split(/\s+/).filter(Boolean)
  if (parts.length === 0) return []
  const out = []
  for (const rec of DIVIDEND_STOCK_SEARCH_POOL) {
    if (out.length >= Math.max(1, Number(limit) || 12)) break
    const hay = `${rec.symbol.toLowerCase()} ${rec.name}`
    if (!parts.every((p) => hay.includes(p))) continue
    out.push({
      ...rec.row,
      dividends: toPresetDividends(rec.row),
    })
  }
  return out
}

export async function fetchStockFromSupabase(input = '') {
  const { supabase } = await import('../lib/supabase')
  const symbol = String(input || '').trim().toUpperCase()
  if (!symbol) return null
  const candidates = /^\d{4}$/.test(symbol) ? [symbol, `${symbol}.T`] : [symbol]
  const { data, error } = await supabase
    .from('stock_symbols')
    .select('symbol,name,region,sector')
    .in('symbol', candidates)
    .limit(2)
  if (error) return null
  const row = (data || [])[0]
  if (!row) return null
  return {
    symbol: String(row.symbol || '').trim().toUpperCase(),
    name: String(row.name || '').trim(),
    region: String(row.region || '').trim().toUpperCase(),
    sector: String(row.sector || '').trim(),
  }
}
